    kb.add_text_button(text="‚è© –ü—Ä–æ–ø—É—Å—Ç–∏—Ç—å", payload={"button": "skip_call_message"})

    kb.add_text_button(text="üö´ –û—Ç–º–µ–Ω–∞", payload={"button": "cancel_call"})
    return kb.get_keyboard()
